/*
    즉시 실행 함수

    - 함수를 정의함과 동시에 바로 실행
    - 최초 한번만 실행하는 코드를 처리하기 적합함..
    - 대다수의 라이브러리가 사용한다.

    형식
    (함수정의)([매개변수값, ...]);
*/
"use strict";
(function () { console.log("즉시 실행 함수 호출됨...") })();
// (() => console.log("즉시 실행 함수 호출됨..."))();

(function (msg) { console.log("즉시 실행 함수 호출됨...", msg) })("테스트");




